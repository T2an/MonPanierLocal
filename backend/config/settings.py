# Utiliser os.getenv avec des valeurs par défaut (compatible avec python-decouple)
def config(key, default=None, cast=None):
    """Fonction helper compatible avec python-decouple"""
    value = os.getenv(key, default)
    if value is None:
        return default
    if cast:
        if cast == bool:
            return str(value).lower() in ('true', '1', 'yes', 'on')
        elif callable(cast):
            return cast(value)
    return value

